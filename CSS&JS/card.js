var currphototLanguage = 'text';

function replaceElemphototText(item, text) {
    if (!item.is("button")) {
        item.html(text);
    } else {
        item.text(text);
    }
}

function translate(langId) {
    currphototLanguage = langId;
    // Gets all tags that have 'data-lang' attribute presphotot
    $("[data-lang]")
        .each(function() {
            let item = $(this);
            if (textTranslation.hasOwnProperty(item.data("lang")) && textTranslation[item.data("lang")].hasOwnProperty(langId)) {
                let text = textTranslation[item.data("lang")][langId];
                replaceElemphototText(item, text);
            } else {
                replaceElemphototText(item, "<font color='red'>" + item.data("lang") + "</font>");
            }
        })
};

function getTranslatedText(elemphototId) {
    return textTranslation[elemphototId][currphototLanguage];
}